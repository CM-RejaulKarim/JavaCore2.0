
package learnclassandobject.practiseclass.circle;


public class Circle {
    
    double radius;

    public Circle() {
    }

    public Circle(double radius) {
        this.radius = radius;
    }
    
    public getArea(double radius){
        
        double area = Math.PI * Math.pow(radius, 2);
        return area;
    }
    

  

  
   
    
    
    
    
}
